package com.bruno.aula;

public class LearningPrimitiveTypes {

    public static void main(String[] args) {

        byte byteType = 127;
        short shortType = 32767;
        char charType = 'C';
        char asciiCode = 36;
        char unicodeValue = '\u0024';
        float floatType = 2.6f;
        double doubleType = 3.59;
        int intType = 2147483647;
        long longType = 9223372036854775807L;
        boolean booleanType = true;

        System.out.println("O código 36 da tabela ascii representa o caracter '" + asciiCode + "'.");
        System.out.println("O código 0024 da tabela unicode representa o caracter '" + unicodeValue + "'.");

    }
}
